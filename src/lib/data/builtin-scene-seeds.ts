import { AIExplanation, Lesson, SentenceChunkDetail } from "@/lib/types";
import { normalizeLessonStructure } from "@/lib/shared/lesson-content";

export interface BuiltinSceneSeedMeta {
  slug: string;
  englishTitle: string;
  chineseTitle: string;
  level: "L0" | "L1" | "L2";
  category: "starter" | "daily_life" | "time_plan" | "social" | "general";
  subcategory?: string;
  sourceType: "builtin";
  isStarter: boolean;
  isFeatured: boolean;
  sortOrder: number;
  estimatedMinutes: number;
  learningGoal: string;
  tags: string[];
}

export interface BuiltinSceneSeed {
  lesson: Lesson;
  meta: BuiltinSceneSeedMeta;
}

interface ChunkDefinition {
  text: string;
  translation: string;
}

interface DialogueSentenceDefinition {
  speaker: "A" | "B";
  text: string;
  translation: string;
  chunks?: string[];
}

interface SceneDefinition {
  meta: Omit<BuiltinSceneSeedMeta, "sourceType">;
  chunks: ChunkDefinition[];
  sentences: DialogueSentenceDefinition[];
}

const findChunkRange = (sentenceText: string, chunkText: string) => {
  const exact = sentenceText.indexOf(chunkText);
  if (exact >= 0) return { start: exact, end: exact + chunkText.length };
  const lowerSentence = sentenceText.toLowerCase();
  const lowerChunk = chunkText.toLowerCase();
  const lowerIndex = lowerSentence.indexOf(lowerChunk);
  if (lowerIndex >= 0) return { start: lowerIndex, end: lowerIndex + chunkText.length };
  return { start: 0, end: Math.min(sentenceText.length, chunkText.length) };
};

const buildChunkDetail = (
  sentenceText: string,
  chunkText: string,
  translation: string,
): SentenceChunkDetail => {
  const range = findChunkRange(sentenceText, chunkText);
  return {
    id: chunkText,
    text: chunkText,
    translation,
    grammarLabel: "Chunk",
    meaningInSentence: `这句话里表示“${translation}”。`,
    usageNote: "先记住这个短语怎么开口，再放回整句里跟读和复述。",
    examples: [
      {
        en: `Try saying "${chunkText}" in your own sentence.`,
        zh: `试着把“${chunkText}”放进你自己的句子里。`,
      },
      {
        en: `I can use "${chunkText}" in a real conversation.`,
        zh: `我可以在真实对话里用“${chunkText}”。`,
      },
    ],
    notes: ["高频口语", "可直接迁移到日常对话"],
    start: range.start,
    end: range.end,
  };
};

const buildExplanations = (chunks: ChunkDefinition[]): AIExplanation[] =>
  chunks.map((chunk) => ({
    key: chunk.text,
    text: chunk.text,
    translation: chunk.translation,
    explanation: "这是一个很常见的日常表达，可直接用于口语交流。",
    examples: [
      `I used "${chunk.text}" in a short dialogue.`,
      `She tried "${chunk.text}" in a real conversation.`,
    ],
    exampleTranslations: [
      `我在一段短对话里用了“${chunk.text}”。`,
      `她在真实对话里试着用了“${chunk.text}”。`,
    ],
    breakdown: ["高频", "日常", "可迁移"],
    pronunciation: "",
    grammarLabel: "Chunk",
  }));

const difficultyByLevel = (level: BuiltinSceneSeedMeta["level"]): Lesson["difficulty"] =>
  level === "L2" ? "Intermediate" : "Beginner";

const buildLesson = (definition: SceneDefinition): Lesson => {
  const chunkMap = new Map(definition.chunks.map((chunk) => [chunk.text, chunk.translation]));
  return normalizeLessonStructure({
    id: definition.meta.slug,
    slug: definition.meta.slug,
    title: `${definition.meta.englishTitle}（${definition.meta.chineseTitle}）`,
    subtitle: definition.meta.learningGoal,
    description: definition.meta.learningGoal,
    difficulty: difficultyByLevel(definition.meta.level),
    estimatedMinutes: definition.meta.estimatedMinutes,
    completionRate: 0,
    tags: definition.meta.tags,
    sceneType: "dialogue",
    sourceType: "builtin",
    sections: [
      {
        id: "section-1",
        title: definition.meta.englishTitle,
        summary: definition.meta.learningGoal,
        blocks: definition.sentences.map((sentence, index) => ({
          id: `block-${index + 1}`,
          kind: "dialogue",
          speaker: sentence.speaker,
          translation: sentence.translation,
          tts: sentence.text,
          sentences: [
            {
              id: `sentence-${index + 1}`,
              speaker: sentence.speaker,
              text: sentence.text,
              translation: sentence.translation,
              tts: sentence.text,
              chunks: sentence.chunks ?? [],
              chunkDetails: (sentence.chunks ?? []).map((chunkText) =>
                buildChunkDetail(
                  sentence.text,
                  chunkText,
                  chunkMap.get(chunkText) ?? "常用表达",
                ),
              ),
            },
          ],
        })),
      },
    ],
    explanations: buildExplanations(definition.chunks),
  });
};

